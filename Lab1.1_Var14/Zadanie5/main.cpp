#include <iostream>

using namespace std;

int main()
{
    float vat;
    cout<< "Enter number of vatts: " ;
    cin>>vat;

    float gcalm = vat/69930069.930069;
    float calh = vat/11.630003082;
    float jh = vat*3600;
    float ergs = vat*10000000;

    cout<<"Gigacalperminute = "<<gcalm<<endl;
    cout<<"Calperhour = "<<calh<<endl;
    cout<<"Joulperhour = "<<jh<<endl;
    cout<<"Ergpersec = "<<ergs<<endl;

    return 0;
}
